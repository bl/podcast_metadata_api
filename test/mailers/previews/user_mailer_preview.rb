class UserMailerPreview < ActionMailer::Preview

  # Preview this email at http://localhost:3000/rails/mailers/user_mailer/account_acivation
  def account_activation
    
    user = User.first # first user is non_activated
    user.update_activation_digest # generate new activation_token (as it is an object attribute, not persistent)
    UserMailer.account_activation(user)
  end

  # Preview this email at http://localhost:3000/rails/mailers/user_mailer/password_reset
  def password_reset
    UserMailer.password_reset
  end

end
